export default function PixelIcon({ variant, color = "currentColor", size = 20, className = "" }: PixelIconProps) {
  const pixels = ICON_DATA[variant] || ICON_DATA.info;
  const gridWidth = 12;
  const gridHeight = 11;
  const pixelWidth = size / gridWidth;
  
  return (
    <span className={`inline-flex items-center justify-center ${className}`} style={{ width: size, height: (size * gridHeight) / gridWidth }}>
      <svg 
        width={size} 
        height={(size * gridHeight) / gridWidth} 
        viewBox={`0 0 ${gridWidth} ${gridHeight}`}
        style={{ imageRendering: "pixelated", shapeRendering: "crispEdges" }}
      >
        {pixels.map((row, y) =>
          row.map((val, x) => {
            if (val === 0) return null;
            let fill = color;
            if (val === 1) fill = "rgba(0,0,0,0.4)"; // Border/Shadow
            return (
              <rect
                key={`${x}-${y}`}
                x={x}
                y={y}
                width={1}
                height={1}
                fill={val === 2 ? color : fill}
              />
            );
          })
        )}
      </svg>
    </span>
  );
}
